lista = [1, 2, 3, 4, 5]

# Check if an element is in the list
a = 3
if a in lista:
    print(f"{a} is in the list")
else:
    print(f"{a} is not in the list")

# Check if an element is not in the list
b = 6
if b not in lista:
    print(f"{b} is not in the list")
else:
    print(f"{b} is in the list")
